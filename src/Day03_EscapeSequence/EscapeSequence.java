package Day03_EscapeSequence;
/*
Escape sequences: Must be given with double quote
    \n: Starts a new line
    \t: tab key(or just hit the tab key)
    \\: Prints single backslash
    \": Double quote(dont forget to close your quote)

 */

public class EscapeSequence {

    public static void main(String[] args) {

        System.out.println("Java \nPython\nC#");
        System.out.println("-----------------------");
        System.out.println("Hello! How are you today? \nI hope you are doing well. \nOkay take care.");
        System.out.println("-----------------------");
        System.out.println("\tJava is a cool programming language");
        System.out.println("-----------------------");
        System.out.println("Let me use a backwards slash real quick\\Boom");
        System.out.println("-----------------------");
        System.out.println("My favorite tv is \"Boy Meets World\"");


    }
}
