package Day19_LoopsRecap_ReDo;

public class LoopsWithoutCurlyBraces {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++)
            // Loops without curly braces will only repeat one statement.
            System.out.println("Wooden");
            System.out.println("Spoon"); // will only print once if there are no curly braces.



    }
}
